package com.iflytek;

public class Variable_Pratice03 {
    public static void main(String[] args) {

            //1.定义字符串变量记录电影的名称
            String movie = "烈日灼心";
            //2.定义三个变量记录主演的名字
            String name1 = "邓超";
            String name2 = "段奕宏";
            String name3 = "郭涛";
            //3. 定义整数类型的变量记录年龄的年份
            int year = 2015;
            //4.定义小数类型的变量记录电影的评分
            double score = 9.0;
        System.out.println("电影名称：" + movie + "，主演：" + name1 + "、" + name2 + "、" + name3 + "，上映年份：" + year + "，评分：" + score);
    }
}

