package com.iflytek;

public class Variable_Pratice02
{
    public static void main(String[] args)
    {
        //1.定义字符串类型的变量记录老师的姓名
        String name = "Muller";
        //2.定义整数类型的变量记录老师的年龄
        int age = 22;
        //3.定义字符类型的变量记录老师的性别
        char gender = '男';
        //4.定义小数类型的变量记录老师的身高
        double height = 180.1;
        //5.定义布尔类型的变量记录老师的婚姻状况
        String situation = "不详";

        System.out.println("我是" + name +"，今年" + age + "岁，身高" + height + "cm，性别" + gender + "，婚姻状况是" + situation + "。");

    }
}

